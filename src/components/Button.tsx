import React from 'react';

interface ButtonProps {
  children: React.ReactNode;
  variant?: 'primary' | 'secondary' | 'outline';
  size?: 'sm' | 'md' | 'lg';
  className?: string;
  onClick?: () => void;
  type?: 'button' | 'submit' | 'reset';
}

const Button: React.FC<ButtonProps> = ({
  children,
  variant = 'primary',
  size = 'md',
  className = '',
  onClick,
  type = 'button',
}) => {
  const baseStyles = 'font-medium rounded-md transition-all duration-300 inline-flex items-center justify-center';
  
  const variantStyles = {
    primary: 'bg-orange-400 hover:bg-orange-500 text-white hover:shadow-lg',
    secondary: 'bg-gray-900 hover:bg-gray-800 text-white hover:shadow-lg',
    outline: 'bg-transparent border-2 border-orange-400 text-orange-400 hover:bg-orange-400 hover:text-white',
  };
  
  const sizeStyles = {
    sm: 'px-3 py-1.5 text-sm',
    md: 'px-4 py-2 text-base',
    lg: 'px-6 py-3 text-lg',
  };
  
  return (
    <button
      type={type}
      className={`${baseStyles} ${variantStyles[variant]} ${sizeStyles[size]} ${className}`}
      onClick={onClick}
    >
      {children}
    </button>
  );
};

export default Button;